//named functions
function function1(x, y) {
    return x + y;
}
//anonym functions
var function2 = function (x, y) {
    return x + y;
};
//arrow functions
var function3 = function (x, y) {
    return x + y;
};
console.log(function1(1, 2));
console.log(function2(1, 2));
console.log(function3(1, 2));
